public class Parte4Condicionalifelse {

    public static void main(String[] args) throws Exception {

        int num1 = 5;
        int num2 = 10;

        if (num2 > num1) {
            System.out.println("El num2 es mayor al num1");
        } else {
            System.out.println("El num2 es menor al num1");
        }
    }
}
